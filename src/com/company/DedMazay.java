package com.company;

public class DedMazay {
   private String name;
   private String surname;

private HOOFG will;


    public DedMazay(String name, String surname, HOOFG will) {
        this.name = name;
        this.surname = surname;
        this.will = will;
    }

    public HOOFG getWill() {
        return will;
    }

    public DedMazay(String name, String surname) {
        this.name = name;
        this.surname = surname;

    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

}
